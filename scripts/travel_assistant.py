def format_hotel_result(data, dest_name: str, check_in: str, check_out: str):
    """格式化酒店搜索结果为Markdown，包含预订链接

    飞猪返回的detailUrl本身就是可直接跳转的预订链接
    """
    if not data or data.get("status") != 0:
        return "❌ 未查询到酒店数据\n"

    hotels = data.get("data", {}).get("itemList", [])
    if not hotels:
        return "❌ 未找到酒店\n"

    lines = []
    lines.append(f"## 🏨 酒店搜索结果\n")
    lines.append(f"**目的地**: {dest_name}  **入住**: {check_in} **退房**: {check_out}\n")
    lines.append(f"| 酒店名称 | 星级 | 价格 | 预订 |\n")
    lines.append(f"|----------|------|------|------|\n")

    for h in hotels[:8]:
        name = h.get("name", "未知")
        star = h.get("star", "-")
        price = h.get("price", "-")
        detail_url = h.get("detailUrl", "")
        if detail_url:
            # flyai 返回的 detailUrl 本身就是飞猪的直接预订链接
            link = f"[预订]({detail_url})"
        else:
            link = "-"
        lines.append(f"| {name} | {star} | ¥{price} | {link} |\n")

    lines.append(f"\n*共 {len(hotels)} 家酒店，显示前 8 个*")
    lines.append("\n💡 点击预订链接可直接跳转到飞猪预订页面，登录后即可下单")
    return "".join(lines)

