    args = parser.parse_args()

    # 解析查询类型
    from_city, to_city, date, query_type = parse_travel_query(args.query)

    # 机票/火车票/酒店查询
    if query_type in ["flight", "train", "hotel"]:
        if not from_city or not to_city:
            print("❌ 无法识别出发地和目的地，请使用格式: 北京到上海 明天机票")
            return

        force_refresh = args.force_refresh
        md = []
        md.append(f"# 🧳 出行查询: {args.query}")
        md.append(f"**查询时间**: {datetime.now().strftime('%Y-%m-%d %H:%M')}\n")

        if query_type == "flight":
            print(f"[INFO] 正在搜索机票: {from_city} → {to_city}, {date}")
