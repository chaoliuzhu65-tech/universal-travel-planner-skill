        html.append("<p><strong>查询关键词:</strong> %s<br><strong>搜索范围:</strong> %gkm</p>" % (query, radius_km))

        for cat, r in report["categories"].items():
            cfg = INDUSTRY_CATEGORIES[cat]
            icon = cfg["icon"]
            html.append(f'<div class="category">')
            html.append(f'<h3>{icon} {cat} - {cfg["description"]}</h3>')
            html.append(f'<p>总数: {r.total_count}家<br>3km内: {r.nearby_count}家<br>完善度: {r.coverage_score}/100</p>')
